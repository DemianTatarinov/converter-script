#!/bin/bash
set -euo pipefail

# 1. Автоматически создаем уникальное имя для новой папки (по дате и времени)
# Пример: backup_2026-06-19_15-30
FOLDER_NAME="backup_$(date +%F_%H-%M)"

echo "--> Создается новая папка: $FOLDER_NAME"
mkdir -p "$FOLDER_NAME"

# 2. Находим все файлы на текущем уровне и перемещаем их в созданную папку
# (Исключаем сам скрипт, скрытые файлы и файлы Git, чтобы репозиторий не ломался)
find . -maxdepth 1 -type f ! -name "$(basename "$0")" ! -name ".*" -exec mv {} "$FOLDER_NAME/" \;

# 3. Автоматически добавляем, коммитим и отправляем изменения в Git
echo "--> Индексация файлов в Git..."
git add .

echo "--> Создание коммита..."
git commit -m "Авто-пуш: создана папка $FOLDER_NAME"

echo "--> Отправка на GitHub..."
git push

echo "🚀 Всё готово! Файлы успешно упакованы в '$FOLDER_NAME' и улетели на Git."
